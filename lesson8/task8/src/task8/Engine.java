package task8;

public class Engine {
    String name;
    int cylinderAmount;
    double cylinderVolume;
    int weight;
    public Engine(){}
    public Engine(String name, int cylinderAmount, double cylinderVolume, int weight) {
        this.name = name;
        this.cylinderAmount = cylinderAmount;
        this.cylinderVolume = cylinderVolume;
        this.weight = weight;
    }
}
